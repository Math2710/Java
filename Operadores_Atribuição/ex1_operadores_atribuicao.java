package Operadores_Atribuição;

public class ex1_operadores_atribuicao {
    public static void main(String[] args) {
        int num = 10;
        System.err.println("Número original: " + num);
        num += 5;
        System.err.println("Num + 5: " + num);
        num -= 3;
        System.err.println("Num - 3: " + num);
        num *= 2;
        System.err.println("Num multiplicado por 2: " + num);
        num /= 4;
        System.err.println("Num divido por 4: " + num);
        num %= 4;
        System.err.println("Sobra de Num divido por 4: " + num);

    }
    
}
